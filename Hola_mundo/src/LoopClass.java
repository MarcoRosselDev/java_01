public class LoopClass {
    public static void main(String[] args) {
        // for
        byte length = 10;
        for (int i = 0; i < length ; i++) {
            System.out.println(i);
        }
        // while
        byte w = 0;
        while (w < 5) {
            System.out.println(w);
            w++;
        }
    }
}